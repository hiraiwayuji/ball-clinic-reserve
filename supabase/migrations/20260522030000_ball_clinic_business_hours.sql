-- ============================================================
-- ボール接骨院の営業時間を明示設定
-- ============================================================
-- 20260522020000 で business_hours カラムを追加した時点では
-- NULL = DEFAULT_SCHEDULE (12:00-22:30 / 10:00-17:30) になっていたが、
-- 実運用は 12:00-23:00 / 10:00-18:00 なので明示設定で揃える。
-- 「今まで通り」の挙動と実運用のズレを解消（2026-05-22 ぼーるくん指摘）。
-- ============================================================

UPDATE public.clinic_settings
   SET business_open_weekday   = '12:00',
       business_close_weekday  = '23:00',
       business_open_saturday  = '10:00',
       business_close_saturday = '18:00',
       closed_weekdays         = '0,3'   -- 日(0), 水(3)。祝日は clinic_holidays で別途
 WHERE id = '00000000-0000-0000-0000-000000000001'
   AND business_open_weekday IS NULL;     -- 既存設定値がある場合は上書きしない
